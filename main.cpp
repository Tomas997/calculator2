#include <iostream>  // Підключаємо бібліотеку для введення-виведення
#include "calculator.h"  // Підключаємо заголовочний файл калькулятора

int main() {
    Calculator calc;  // Створюємо об'єкт калькулятора
    double a = 5.0, b = 3.0;  // Приклад значень

    // Виконуємо додавання та виводимо результат
    std::cout << "Add: " << calc.Add(a, b) << std::endl;
    // Виконуємо віднімання та виводимо результат
    std::cout << "Sub: " << calc.Sub(a, b) << std::endl;

    return 0;  // Вихід з програми
}
